package com.kishannareshpal.horizontalbarchartview.models;

public class MyData {

    private int id;
    private float percentage;


    public MyData(int id, float percentage) {
        this.id = id;
        this.percentage = percentage;
    }

    public int getId() {
        return id;
    }

    public float getPercentage() {
        return percentage;
    }

}
